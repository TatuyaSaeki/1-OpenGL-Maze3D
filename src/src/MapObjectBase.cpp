{
}

//======================================================================================
//! 初期化
//======================================================================================
bool MapObjectBase::init()
{
	_objectType = ObjectType::Wall;
	
	_texture = new Texture();
	if (_texture->load("data/Texture/Map/wallImage1.png") == false) {
		MessageBox(nullptr, "png.png ファイルの読み込みに失敗しました.", "エラー", MB_OK);
		return false;
	}

	return true;
}

